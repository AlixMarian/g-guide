// AutoGen.jsx

import { useState, useEffect } from 'react';
import { Button, Form } from 'react-bootstrap';
import AutocompleteSearch from '@components/mapfiles/AutocompleteSearch';
import PropTypes from 'prop-types';


const AutoGen = ({ onBack, churches, currentPosition, handleAutoGeneratedRoute }) => {
  const [usingCurrentLocation, setUsingCurrentLocation] = useState(true);
  const [startLocation, setStartLocation] = useState(null);
  const [startChurchId, setStartChurchId] = useState('');

  const [usingCustomEndLocation, setUsingCustomEndLocation] = useState(false);
  const [endLocation, setEndLocation] = useState(null);
  const [endChurchId, setEndChurchId] = useState('');

  const [sortedChurches, setSortedChurches] = useState([]);

  useEffect(() => {
    // Once start and end locations are set, sort the churches
    const origin = usingCurrentLocation ? currentPosition : startLocation;
    if (origin && endLocation) {
      sortAndSetTopChurches(churches);
    }
  }, [usingCurrentLocation, startLocation, endLocation, churches]);

  const sortAndSetTopChurches = (churchesList) => {
    if (window.google) {
      const origin = usingCurrentLocation ? currentPosition : startLocation;

      const currentLatLng = new window.google.maps.LatLng(origin.lat, origin.lng);
      const destinationLatLng = new window.google.maps.LatLng(endLocation.lat, endLocation.lng);

      const sortedChurches = churchesList.map((church) => {
        // Ensure latitude and longitude are numbers
        const latitude = parseFloat(church.latitude);
        const longitude = parseFloat(church.longitude);

        // Check if lat/long values are valid numbers
        if (!isNaN(latitude) && !isNaN(longitude)) {
          const churchLatLng = new window.google.maps.LatLng(latitude, longitude);

          // Calculate distance from start location to church
          const distanceFromStart = window.google.maps.geometry.spherical.computeDistanceBetween(
            churchLatLng,
            currentLatLng
          );

          // Calculate distance from church to end location
          const distanceToEnd = window.google.maps.geometry.spherical.computeDistanceBetween(
            churchLatLng,
            destinationLatLng
          );

          // Total distance
          const totalDistance = (distanceFromStart + distanceToEnd) / 1000; // Convert to km

          return {
            ...church,
            distance: totalDistance,
          };
        } else {
          console.warn(`Invalid latitude or longitude for church: ${church.churchName}`);
          return {
            ...church,
            distance: Infinity, // Set distance to a high value if lat/long are invalid
          };
        }
      });

      // Sort the churches by distance in ascending order
      sortedChurches.sort((a, b) => a.distance - b.distance);

      console.log('Sorted Churches:', sortedChurches);

      // Set the top 5 nearest churches
      setSortedChurches(sortedChurches.slice(0, 6));
    }
  };

  const handleGenerateRoute = () => {
    const origin = usingCurrentLocation ? currentPosition : startLocation;
    if (!origin || !endLocation || sortedChurches.length === 0) {
      alert('Please select valid start and end locations.');
      return;
    }

    if (handleAutoGeneratedRoute) {
      handleAutoGeneratedRoute(origin, sortedChurches, endLocation);
    }
  };

  return (
    <div
      style={{
        position: 'fixed',
        top: 0,
        left: 0,
        width: '100%',
        maxWidth: '400px',
        height: '100%',
        backgroundColor: '#FFFFFF',
        zIndex: 1000,
        padding: '1rem',
        overflowY: 'auto',
        boxShadow: '0 0 15px rgba(0,0,0,0.2)',
        borderRight: '1px solid #e0e0e0',
      }}
    >
      <div className="d-flex justify-content-between align-items-center mb-4">
        <Button variant="outline-secondary" onClick={onBack}>
          <i className="bi bi-arrow-left me-2"></i> Back
        </Button>
        <h5 className="mb-0 fw-bold">Auto Generate Route</h5>
      </div>

      <Form>
        {/* Start Location Section */}
        <Form.Group controlId="use-current-location" className="mb-3">
          <Form.Check
            type="checkbox"
            label="Use Current Location"
            checked={usingCurrentLocation}
            onChange={() => {
              setUsingCurrentLocation(!usingCurrentLocation);
              setStartChurchId('');
              setStartLocation(null);
            }}
          />
        </Form.Group>

        {!usingCurrentLocation && (
          <Form.Group controlId="start-church" className="mb-3">
            <Form.Label>Select Start Church</Form.Label>
            <Form.Control
              as="select"
              value={startChurchId}
              onChange={(e) => {
                const selectedChurchId = e.target.value;
                setStartChurchId(selectedChurchId);
                const selectedChurch = churches.find((church) => church.id === selectedChurchId);
                if (selectedChurch) {
                  setStartLocation({
                    lat: parseFloat(selectedChurch.latitude),
                    lng: parseFloat(selectedChurch.longitude),
                  });
                }
              }}
            >
              <option value="">Select Church</option>
              {churches.map((church) => (
                <option key={church.id} value={church.id}>
                  {church.churchName}
                </option>
              ))}
            </Form.Control>
          </Form.Group>
        )}

        {/* End Location Section */}
        <Form.Group controlId="use-custom-end-location" className="mb-3">
          <Form.Check
            type="checkbox"
            label="Use Custom End Location"
            checked={usingCustomEndLocation}
            onChange={() => {
              setUsingCustomEndLocation(!usingCustomEndLocation);
              setEndChurchId('');
              setEndLocation(null);
            }}
          />
        </Form.Group>

        {!usingCustomEndLocation ? (
          <Form.Group controlId="end-church" className="mb-3">
            <Form.Label>Select End Church</Form.Label>
            <Form.Control
              as="select"
              value={endChurchId}
              onChange={(e) => {
                const selectedChurchId = e.target.value;
                setEndChurchId(selectedChurchId);
                const selectedChurch = churches.find((church) => church.id === selectedChurchId);
                if (selectedChurch) {
                  setEndLocation({
                    lat: parseFloat(selectedChurch.latitude),
                    lng: parseFloat(selectedChurch.longitude),
                  });
                }
              }}
            >
              <option value="">Select Church</option>
              {churches.map((church) => (
                <option key={church.id} value={church.id}>
                  {church.churchName}
                </option>
              ))}
            </Form.Control>
          </Form.Group>
        ) : (
          <Form.Group controlId="end-location-custom" className="mb-3">
            <Form.Label>Enter End Location</Form.Label>
            <AutocompleteSearch
              onPlaceSelected={(location) => {
                console.log('Selected end location:', location);
                setEndLocation(location);
              }}
              placeholder="Enter end location"
            />
          </Form.Group>
        )}

        {/* Generate Route Button */}
        <Button variant="primary" className="w-100 mt-3" onClick={handleGenerateRoute}>
          Generate Route
        </Button>

        {/* Display Sorted Churches */}
        {sortedChurches.length > 0 && (
          <div className="mt-4">
            <h5>Top 5 Nearest Churches:</h5>
            <ul>
              {sortedChurches.map((church) => (
                <li key={church.id}>
                  {church.churchName} - {church.distance.toFixed(2)} km
                </li>
              ))}
            </ul>
          </div>
        )}
      </Form>
    </div>
  );
};

AutoGen.propTypes = {
  onBack: PropTypes.func.isRequired,
  churches: PropTypes.arrayOf(
    PropTypes.shape({
      id: PropTypes.string.isRequired,
      churchName: PropTypes.string.isRequired,
      latitude: PropTypes.string.isRequired,
      longitude: PropTypes.string.isRequired,
    })
  ).isRequired,
  currentPosition: PropTypes.shape({
    lat: PropTypes.number.isRequired,
    lng: PropTypes.number.isRequired,
  }),
  handleAutoGeneratedRoute: PropTypes.func.isRequired,
};

// Default props for optional values
AutoGen.defaultProps = {
  currentPosition: null,
};

export default AutoGen;
